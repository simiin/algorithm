#include <iostream>

using namespace std;

int main(void) {

	int a = 0, b = 0, c = 0;

	cin >> a >> b >> c;
	
	if ((a % 2) == 0) {
		cout << "odd" << '\n';
	} else {
		cout << "even" << '\n';
	}
	
	if ((b % 2) == 0) {
		cout << "odd" << '\n';
	} else {
		cout << "even" << '\n';
	}
	
	if ((c % 2) == 0) {
		cout << "odd" << '\n';
	} else {
		cout << "even" << '\n';
	}

	return 0;

}
